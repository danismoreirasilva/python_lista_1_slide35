num1 = int(input('Digite o primeiro valor: '))
num2 = int(input('Digite o segundo valor: '))
num3 = int(input('Digite o terceiro valor: '))

soma = num1+num2+num3

print(f'A soma entre os valores ({num1},{num2},{num3}) é: {soma}')
print(f'A média entre os valores ({num1},{num2},{num3}) é: {(soma)/3:.2f}')